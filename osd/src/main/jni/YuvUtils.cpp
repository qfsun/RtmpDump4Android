/**
 * 查找索引
 * @param c     字符
 * @return      返回索引位置
 */
int getIndex(jchar c) {
    if (c >= '0' && c <= '9')
        return c - '0';
    else if (c == '-')
        return 10;
    else if (c == ' ')
        return 11;
    else if (c == ':')
        return 12;
    else if (c == 24180)//年
        return 13;
    else if (c == 26376)//'月'
        return 14;
    else if (c == 26085)//'日'
        return 15;
    return 11;
}

/**
 * 添加时间水印
 * @param nv21Src   android摄像机获取的NV21格式yuv数据
 * @param destData  转换后的数据
 * @param date  当前的时间
 */
void YuvUtils::addOsd(JNIEnv *env, jbyteArray yuv_in_data, jbyteArray yvu_out_data,
                      jstring date_) {
    jbyte *nv21Src = env->GetByteArrayElements(yuv_in_data, NULL);
    jbyte *destData = env->GetByteArrayElements(yvu_out_data, NULL);
    const jchar *date = env->GetStringChars(date_, NULL);

    if (mNumArrays == NULL) {
        loge("mNumArrays == NULL");
    }
    //添加时间水印
    jbyte *dest = destData;
    jbyte *start = dest + off_y * frame_width;
    for (int i = 0; i < date_len; i++) {
        int index = getIndex(*(date + i));
        char *num = mNumArrays + size * index;
        jbyte *column = start + i * num_width;
        if (!num)
            continue;
        for (int j = 0; j < num_height; j++) {
            jbyte *destIndex = column + j * frame_width + off_x;
            char *src = num + j * num_width;
            for (int k = 0; k < num_width; k++) {
                if (*(src + k) != 0) {//黑色背景色
                    *(destIndex + k) = -21;//水印文字颜色，-21为白色，0为黑色
                }
            }
        }
    }
    env->ReleaseByteArrayElements(yuv_in_data, nv21Src, 0);
    env->ReleaseByteArrayElements(yvu_out_data, destData, 0);
    env->ReleaseStringChars(date_, date);
}

void YuvUtils::releaseOsd() {
    if (!is_init) {
        loge("releaseOsd . is_init = false");
        return;
    }
    is_init = false;
    free(mNumArrays);
}




